/**
 * Функція для створення привітання
 *  msg - Привітальне слово (Hi, Hello, Hey тощо)
 *  name - Ім'я людини
 *  Повне привітання у форматі "повідомлення, ім'я"
 *
 * Приклад: greet('Hello', 'John') → "Hello, John"
 */
function greet(msg, name) {
  // Конкатенація (додавання) рядків
  return msg + ', ' + name;
}

console.log('=== Тестування функції greet ===');
console.log(greet('Hi', 'John')); // Очікуваний результат: "Hi, John"
console.log(greet('Hey', 'Bob')); // Очікуваний результат: "Hey, Bob"
console.log(greet('Hello', 'Mary')); // Очікуваний результат: "Hello, Mary"
console.log(greet('Привіт', 'Олег')); // Працює з будь-якою мовою
console.log(greet('', 'Анонім')); // Тест з порожнім привітанням

