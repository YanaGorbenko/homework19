/**
 * Генерує випадкове ціле число в заданому діапазоні (включно)
 *  min - Мінімальне значення (включно)
 *  max - Максимальне значення (включно)
 *  Випадкове ціле число в діапазоні [min, max]
 *
 * Математика формули:
 * 1. Math.random() генерує число від 0 (включно) до 1 (виключно)
 * 2. max - min + 1 - кількість можливих значень
 * 3. Math.floor() округлює вниз до цілого
 * 4. + min зсуває діапазон до потрібних меж
 */
function getRandomInt(min, max) {
  // Основна формула генерації випадкового числа
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

// Тестування функції getRandomInt
console.log('\n=== Тестування функції getRandomInt ===');

console.log('Тест 1 - Базові приклади:');
console.log(`Випадкове число від 1 до 10: ${getRandomInt(1, 10)}`);
console.log(`Випадкове число від 40 до 50: ${getRandomInt(40, 50)}`);
console.log(`Випадкове число від 1 до 100: ${getRandomInt(1, 100)}`);

console.log('Тест 2 - Перевірка на те, що кожного разу інше число:');
console.log(`Випадкове число від 1 до 10: ${getRandomInt(1, 10)}`);
console.log(`Випадкове число від 40 до 50: ${getRandomInt(40, 50)}`);
console.log(`Випадкове число від 1 до 100: ${getRandomInt(1, 100)}`);
