/**
 * Функція для додавання великих цілих чисел, представлених у вигляді рядків
 *  numStr1 - Перше число як рядок
 *  numStr2 - Друге число як рядок
 *  Сума чисел у вигляді BigInt
 */
function sumBigIntegers(numStr1, numStr2) {
  // Конвертуємо рядки в BigInt
  let numBigInt1 = BigInt(numStr1);
  let numBigInt2 = BigInt(numStr2);

  // Виконуємо додавання
  return numBigInt1 + numBigInt2;
}

console.log('\n=== Тестування функції sumBigIntegers ===');

console.log('Тест 1 - Великі числа:');
console.log(sumBigIntegers('9007199254740991', '9007199254740991'));
// Результат: 18014398509481982n

console.log('\nТест 2 - Малі числа:');
console.log(sumBigIntegers('100', '200')); // 300n
console.log(sumBigIntegers('0', '100')); // 100n

